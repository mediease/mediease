/**
 * @desc    Create new appointment (Nurse only)
 * @route   POST /fhir/Appointment
 * @access  Private/Nurse
 */
export const createAppointment = asyncHandler(async (req, res) => {
  const { patientPhn, doctorLicense, nurseId, roomNo, type, appointmentDate } = req.body;

  // Verify nurse ID matches logged-in user
  // Normalize both values for comparison (handle case where DB has "444444" but form sends "NUR44444")
  if (req.user.role === 'nurse') {
    const userNurId = String(req.user.nurId || '').trim();
    const submittedNurId = String(nurseId || '').trim();
    
    // Extract numeric part for comparison (handles both "444444" and "NUR44444")
    const userNurIdNum = userNurId.replace(/^NUR/i, '');
    const submittedNurIdNum = submittedNurId.replace(/^NUR/i, '');
    
    // Also check exact match and normalized match
    if (userNurId !== submittedNurId && 
        userNurId.toUpperCase() !== submittedNurId.toUpperCase() &&
        userNurIdNum !== submittedNurIdNum) {
      return res.status(403).json({
        success: false,
        message: `Nurse ID does not match your credentials. Your ID: ${userNurId}, Submitted: ${submittedNurId}`
      });
    }
  }

  // Verify patient exists
  const patient = await FHIRPatient.findOne({ phn: patientPhn });
  if (!patient) {
    return res.status(404).json({
      success: false,
      message: `Patient not found with PHN: ${patientPhn}`
    });
  }

  // Verify doctor exists and is approved
  const doctor = await FHIRPractitioner.findOne({ medicalLicenseId: doctorLicense, role: 'doctor' });
  if (!doctor) {
    return res.status(404).json({
      success: false,
      message: `Doctor not found with Medical License: ${doctorLicense}`
    });
  }

  // Verify nurse exists and is approved
  const nurse = await FHIRPractitioner.findOne({ nurId: nurseId, role: 'nurse' });
  if (!nurse) {
    return res.status(404).json({
      success: false,
      message: `Nurse not found with ID: ${nurseId}`
    });
  }

  // Create appointment (APID will be auto-generated by pre-save hook)
  const appointmentData = {
    patientPhn,
    doctorLicense,
    nurseId,
    roomNo,
    type: type || 'general',
    appointmentDate: appointmentDate || new Date(),
    status: 'pending'
  };

  const tempAppointment = new FHIRAppointment(appointmentData);
  const apid = tempAppointment.apid || await createAPID();

  // Create FHIR resource
  const fhirResource = createFHIRAppointment({
    ...appointmentData,
    appointmentDate: appointmentData.appointmentDate
  }, apid);

  // Create appointment with FHIR resource
  const appointment = await FHIRAppointment.create({
    ...appointmentData,
    apid,
    resource: fhirResource
  });

  res.status(201).json({
    success: true,
    message: 'Appointment created successfully',
    data: {
      id: appointment._id,
      apid: appointment.apid,
      resource: appointment.resource,
      metadata: {
        patientPhn: appointment.patientPhn,
        doctorLicense: appointment.doctorLicense,
        nurseId: appointment.nurseId,
        roomNo: appointment.roomNo,
        type: appointment.type,
        status: appointment.status,
        appointmentDate: appointment.appointmentDate
      }
    }
  });
});

/**
 * @desc    Get appointment by APID or ID, or list appointments by nurseId
 * @route   GET /fhir/Appointment/:id
 * @route   GET /fhir/Appointment?identifier=AP00001
 * @route   GET /fhir/Appointment?nurseId=444444
 * @access  Private
 */
export const getAppointment = asyncHandler(async (req, res) => {
  const { id } = req.params;
  const { identifier, nurseId, status, page = 1, limit = 100 } = req.query;

  // If nurseId is provided, return list of appointments for that nurse
  if (nurseId) {
    // If user is a nurse, verify they can only access their own appointments
    let targetNurseId = String(nurseId).trim();
    if (req.user.role === 'nurse' && req.user.nurId) {
      // Nurses can only access their own appointments - use ID from JWT token
      targetNurseId = String(req.user.nurId).trim();
    } else if (req.user.role === 'lab_assistant') {
      // Lab assistants don't have nurseId, return empty list
      return res.json({
        success: true,
        count: 0,
        total: 0,
        totalPages: 0,
        currentPage: page,
        data: []
      });
    }

    const query = { nurseId: targetNurseId };
    if (status) {
      query.status = status;
    }

    const appointments = await FHIRAppointment.find(query)
      .limit(limit * 1)
      .skip((page - 1) * limit)
      .sort({ appointmentDate: -1 });

    const count = await FHIRAppointment.countDocuments(query);

    return res.json({
      success: true,
      count: appointments.length,
      total: count,
      totalPages: Math.ceil(count / limit),
      currentPage: page,
      data: appointments.map(apt => ({
        id: apt._id,
        apid: apt.apid,
        resource: apt.resource,
        metadata: {
          patientPhn: apt.patientPhn,
          doctorLicense: apt.doctorLicense,
          nurseId: apt.nurseId,
          roomNo: apt.roomNo,
          type: apt.type,
          status: apt.status,
          appointmentDate: apt.appointmentDate
        }
      }))
    });
  }

  // Otherwise, get single appointment by ID or identifier
  let appointment;

  if (identifier) {
    appointment = await FHIRAppointment.findOne({ apid: identifier });
  } else if (id) {
    if (id.match(/^AP\d{5}$/)) {
      appointment = await FHIRAppointment.findOne({ apid: id });
    } else {
      appointment = await FHIRAppointment.findById(id);
    }
  } else {
    return res.status(400).json({
      success: false,
      message: 'Please provide appointment ID, identifier (APID), or nurseId'
    });
  }

  if (!appointment) {
    return res.status(404).json({
      success: false,
      message: 'Appointment not found'
    });
  }

  res.json({
    success: true,
    data: {
      id: appointment._id,
      apid: appointment.apid,
      resource: appointment.resource,
      metadata: {
        patientPhn: appointment.patientPhn,
        doctorLicense: appointment.doctorLicense,
        nurseId: appointment.nurseId,
        roomNo: appointment.roomNo,
        type: appointment.type,
        status: appointment.status,
        appointmentDate: appointment.appointmentDate
      }
    }
  });
});

