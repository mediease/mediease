import asyncHandler from 'express-async-handler';
import FHIRAppointment from '../models/fhirAppointment.model.js';
import FHIRPatient from '../models/fhirPatient.model.js';
import FHIRPractitioner from '../models/fhirPractitioner.model.js';
import User from '../models/user.model.js';
import { createFHIRAppointment } from '../utils/fhirHelpers.js';
import { createAPID } from '../utils/idGenerators.js';

/**
 * @desc    Create new appointment (Nurse only)
 * @route   POST /fhir/Appointment
 * @access  Private/Nurse
 */
export const createAppointment = asyncHandler(async (req, res) => {
  const { patientPhn, doctorLicense, nurseId, roomNo, type, appointmentDate } = req.body;

  // Verify nurse ID matches logged-in user
  if (req.user.role === 'nurse' && req.user.nurId !== nurseId) {
    return res.status(403).json({
      success: false,
      message: 'Nurse ID does not match your credentials'
    });
  }

  // Verify patient exists
  const patient = await FHIRPatient.findOne({ phn: patientPhn });
  if (!patient) {
    return res.status(404).json({
      success: false,
      message: `Patient not found with PHN: ${patientPhn}`
    });
  }

  // Verify doctor exists and is approved
  const doctor = await FHIRPractitioner.findOne({ medicalLicenseId: doctorLicense, role: 'doctor' });
  if (!doctor) {
    return res.status(404).json({
      success: false,
      message: `Doctor not found with Medical License: ${doctorLicense}`
    });
  }

  // Verify nurse exists and is approved
  const nurse = await FHIRPractitioner.findOne({ nurId: nurseId, role: 'nurse' });
  if (!nurse) {
    return res.status(404).json({
      success: false,
      message: `Nurse not found with ID: ${nurseId}`
    });
  }

  // Create appointment (APID will be auto-generated by pre-save hook)
  const appointmentData = {
    patientPhn,
    doctorLicense,
    nurseId,
    roomNo,
    type: type || 'general',
    appointmentDate: appointmentDate || new Date(),
    status: 'pending'
  };

  const tempAppointment = new FHIRAppointment(appointmentData);
  const apid = tempAppointment.apid || await createAPID();

  // Create FHIR resource
  const fhirResource = createFHIRAppointment({
    ...appointmentData,
    appointmentDate: appointmentData.appointmentDate
  }, apid);

  // Create appointment with FHIR resource
  const appointment = await FHIRAppointment.create({
    ...appointmentData,
    apid,
    resource: fhirResource
  });

  res.status(201).json({
    success: true,
    message: 'Appointment created successfully',
    data: {
      id: appointment._id,
      apid: appointment.apid,
      resource: appointment.resource,
      metadata: {
        patientPhn: appointment.patientPhn,
        doctorLicense: appointment.doctorLicense,
        nurseId: appointment.nurseId,
        roomNo: appointment.roomNo,
        type: appointment.type,
        status: appointment.status,
        appointmentDate: appointment.appointmentDate
      }
    }
  });
});

/**
 * @desc    Get appointment by APID or ID
 * @route   GET /fhir/Appointment/:id
 * @route   GET /fhir/Appointment?identifier=AP00001
 * @access  Private
 */
export const getAppointment = asyncHandler(async (req, res) => {
  const { id } = req.params;
  const { identifier } = req.query;

  let appointment;

  if (identifier) {
    appointment = await FHIRAppointment.findOne({ apid: identifier });
  } else if (id) {
    if (id.match(/^AP\d{5}$/)) {
      appointment = await FHIRAppointment.findOne({ apid: id });
    } else {
      appointment = await FHIRAppointment.findById(id);
    }
  } else {
    return res.status(400).json({
      success: false,
      message: 'Please provide appointment ID or identifier (APID)'
    });
  }

  if (!appointment) {
    return res.status(404).json({
      success: false,
      message: 'Appointment not found'
    });
  }

  res.json({
    success: true,
    data: {
      id: appointment._id,
      apid: appointment.apid,
      resource: appointment.resource,
      metadata: {
        patientPhn: appointment.patientPhn,
        doctorLicense: appointment.doctorLicense,
        nurseId: appointment.nurseId,
        roomNo: appointment.roomNo,
        type: appointment.type,
        status: appointment.status,
        appointmentDate: appointment.appointmentDate
      }
    }
  });
});

/**
 * @desc    Get doctor's appointments by medical license ID
 * @route   GET /fhir/Appointment?practitionerLicense=MED12345
 * @route   GET /doctor/appointments/:medicalLicenseId
 * @access  Private/Doctor
 */
export const getDoctorAppointments = asyncHandler(async (req, res) => {
  const { medicalLicenseId } = req.params;
  const { practitionerLicense } = req.query;
  const { status, page = 1, limit = 10 } = req.query;

  const doctorLicense = medicalLicenseId || practitionerLicense;

  if (!doctorLicense) {
    return res.status(400).json({
      success: false,
      message: 'Medical License ID is required'
    });
  }

  // If user is a doctor, verify they can only access their own appointments
  if (req.user.role === 'doctor' && req.user.medicalLicenseId !== doctorLicense) {
    return res.status(403).json({
      success: false,
      message: 'You can only access your own appointments'
    });
  }

  const query = { doctorLicense };

  if (status) {
    query.status = status;
  }

  const appointments = await FHIRAppointment.find(query)
    .limit(limit * 1)
    .skip((page - 1) * limit)
    .sort({ appointmentDate: -1 });

  const count = await FHIRAppointment.countDocuments(query);

  res.json({
    success: true,
    count: appointments.length,
    total: count,
    totalPages: Math.ceil(count / limit),
    currentPage: page,
    data: appointments.map(apt => ({
      id: apt._id,
      apid: apt.apid,
      resource: apt.resource,
      metadata: {
        patientPhn: apt.patientPhn,
        doctorLicense: apt.doctorLicense,
        nurseId: apt.nurseId,
        roomNo: apt.roomNo,
        type: apt.type,
        status: apt.status,
        appointmentDate: apt.appointmentDate
      }
    }))
  });
});

