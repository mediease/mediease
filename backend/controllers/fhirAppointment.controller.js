/**
 * @desc    Update appointment status
 * @route   PUT /fhir/Appointment/:id
 * @access  Private
 */
export const updateAppointment = asyncHandler(async (req, res) => {
  const { id } = req.params;
  const { status, roomNo, appointmentDate } = req.body;

  let appointment;
  if (id.match(/^AP\d{5}$/)) {
    appointment = await FHIRAppointment.findOne({ apid: id });
  } else {
    appointment = await FHIRAppointment.findById(id);
  }

  if (!appointment) {
    return res.status(404).json({
      success: false,
      message: 'Appointment not found'
    });
  }

  // Update allowed fields
  if (status) {
    appointment.status = status;
    appointment.resource.status = status;
  }

  if (roomNo) {
    appointment.roomNo = roomNo;
  }

  if (appointmentDate) {
    appointment.appointmentDate = appointmentDate;
  }

  await appointment.save();

  res.json({
    success: true,
    message: 'Appointment updated successfully',
    data: {
      id: appointment._id,
      apid: appointment.apid,
      resource: appointment.resource,
      metadata: {
        status: appointment.status
      }
    }
  });
});

/**
 * @desc    Cancel appointment
 * @route   DELETE /fhir/Appointment/:id
 * @access  Private
 */
export const cancelAppointment = asyncHandler(async (req, res) => {
  const { id } = req.params;

  let appointment;
  if (id.match(/^AP\d{5}$/)) {
    appointment = await FHIRAppointment.findOne({ apid: id });
  } else {
    appointment = await FHIRAppointment.findById(id);
  }

  if (!appointment) {
    return res.status(404).json({
      success: false,
      message: 'Appointment not found'
    });
  }

  // Update status to cancelled instead of deleting
  appointment.status = 'cancelled';
  appointment.resource.status = 'cancelled';
  await appointment.save();

  res.json({
    success: true,
    message: 'Appointment cancelled successfully',
    data: {
      apid: appointment.apid,
      status: appointment.status
    }
  });
});
