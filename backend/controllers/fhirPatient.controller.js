import asyncHandler from 'express-async-handler';
import FHIRPatient from '../models/fhirPatient.model.js';
import { createFHIRPatient } from '../utils/fhirHelpers.js';
import { createPHN } from '../utils/idGenerators.js';

/**
 * @desc    Create new patient
 * @route   POST /fhir/Patient
 * @access  Private (nurse/doctor/admin)
 */
export const createPatient = asyncHandler(async (req, res) => {
  const {
    nic,
    firstName,
    lastName,
    gender,
    contactNumber,
    dob,
    address,
    guardianNIC,
    guardianName,
    height,
    weight,
    bloodPressure,
    sugarLevel
  } = req.body;

  // Check if patient with same NIC already exists
  const existingPatient = await FHIRPatient.findOne({ nic });
  if (existingPatient) {
    return res.status(400).json({
      success: false,
      message: 'Patient with this NIC already exists',
      data: {
        phn: existingPatient.phn
      }
    });
  }

  // Create patient (PHN will be auto-generated by pre-save hook)
  const patientData = {
    nic,
    firstName,
    lastName,
    gender: gender.toLowerCase(),
    contactNumber,
    birthDate: dob,
    address,
    guardianNIC,
    guardianName,
    height,
    weight,
    bloodPressure,
    sugarLevel
  };

  // Create a temporary patient to get the PHN
  const tempPatient = new FHIRPatient(patientData);
  const phn = tempPatient.phn || await createPHN();

  // Create FHIR resource
  const fhirResource = createFHIRPatient(req.body, phn);

  // Create patient with FHIR resource
  const patient = await FHIRPatient.create({
    ...patientData,
    phn,
    resource: fhirResource
  });

  res.status(201).json({
    success: true,
    message: 'Patient created successfully',
    data: {
      id: patient._id,
      phn: patient.phn,
      resource: patient.resource,
      metadata: {
        firstName: patient.firstName,
        lastName: patient.lastName,
        gender: patient.gender,
        birthDate: patient.birthDate,
        contactNumber: patient.contactNumber
      }
    }
  });
});

/**
 * @desc    Get patient by ID or identifier (PHN)
 * @route   GET /fhir/Patient/:id
 * @route   GET /fhir/Patient?identifier=PHxxxxx
 * @access  Private
 */
export const getPatient = asyncHandler(async (req, res) => {
  const { id } = req.params;
  const { identifier } = req.query;

  let patient;

  if (identifier) {
    // Search by PHN
    patient = await FHIRPatient.findOne({ phn: identifier });
  } else if (id) {
    // Search by MongoDB ID or PHN
    if (id.match(/^PH\d{5}$/)) {
      patient = await FHIRPatient.findOne({ phn: id });
    } else {
      patient = await FHIRPatient.findById(id);
    }
  } else {
    return res.status(400).json({
      success: false,
      message: 'Please provide patient ID or identifier (PHN)'
    });
  }

  if (!patient) {
    return res.status(404).json({
      success: false,
      message: 'Patient not found'
    });
  }

  res.json({
    success: true,
    data: {
      id: patient._id,
      phn: patient.phn,
      resource: patient.resource,
      metadata: {
        firstName: patient.firstName,
        lastName: patient.lastName,
        gender: patient.gender,
        birthDate: patient.birthDate,
        contactNumber: patient.contactNumber,
        address: patient.address
      }
    }
  });
});

/**
 * @desc    Search patients
 * @route   GET /fhir/Patient
 * @access  Private
 */
export const searchPatients = asyncHandler(async (req, res) => {
  const { name, nic, gender, page = 1, limit = 10 } = req.query;

  const query = {};

  if (name) {
    query.$or = [
      { firstName: { $regex: name, $options: 'i' } },
      { lastName: { $regex: name, $options: 'i' } }
    ];
  }

  if (nic) {
    query.nic = nic;
  }

  if (gender) {
    query.gender = gender.toLowerCase();
  }

  const patients = await FHIRPatient.find(query)
    .limit(limit * 1)
    .skip((page - 1) * limit)
    .sort({ createdAt: -1 });

  const count = await FHIRPatient.countDocuments(query);

  res.json({
    success: true,
    count: patients.length,
    total: count,
    totalPages: Math.ceil(count / limit),
    currentPage: page,
    data: patients.map(patient => ({
      id: patient._id,
      phn: patient.phn,
      resource: patient.resource,
      metadata: {
        firstName: patient.firstName,
        lastName: patient.lastName,
        gender: patient.gender,
        birthDate: patient.birthDate,
        contactNumber: patient.contactNumber
      }
    }))
  });
});

/**
 * @desc    Update patient
 * @route   PUT /fhir/Patient/:id
 * @access  Private
 */
export const updatePatient = asyncHandler(async (req, res) => {
  const { id } = req.params;

  let patient;
  if (id.match(/^PH\d{5}$/)) {
    patient = await FHIRPatient.findOne({ phn: id });
  } else {
    patient = await FHIRPatient.findById(id);
  }

  if (!patient) {
    return res.status(404).json({
      success: false,
      message: 'Patient not found'
    });
  }

  // Update allowed fields
  const allowedUpdates = [
    'contactNumber',
    'address',
    'guardianNIC',
    'guardianName',
    'height',
    'weight',
    'bloodPressure',
    'sugarLevel'
  ];

  allowedUpdates.forEach(field => {
    if (req.body[field] !== undefined) {
      patient[field] = req.body[field];
    }
  });

  // Recreate FHIR resource with updated data
  const updatedFhirResource = createFHIRPatient({
    nic: patient.nic,
    firstName: patient.firstName,
    lastName: patient.lastName,
    gender: patient.gender,
    contactNumber: patient.contactNumber,
    dob: patient.birthDate,
    address: patient.address,
    guardianNIC: patient.guardianNIC,
    guardianName: patient.guardianName,
    height: patient.height,
    weight: patient.weight,
    bloodPressure: patient.bloodPressure,
    sugarLevel: patient.sugarLevel
  }, patient.phn);

  patient.resource = updatedFhirResource;
  await patient.save();

  res.json({
    success: true,
    message: 'Patient updated successfully',
    data: {
      id: patient._id,
      phn: patient.phn,
      resource: patient.resource
    }
  });
});

/**
 * @desc    Delete patient
 * @route   DELETE /fhir/Patient/:id
 * @access  Private/Admin
 */
export const deletePatient = asyncHandler(async (req, res) => {
  const { id } = req.params;

  let patient;
  if (id.match(/^PH\d{5}$/)) {
    patient = await FHIRPatient.findOneAndDelete({ phn: id });
  } else {
    patient = await FHIRPatient.findByIdAndDelete(id);
  }

  if (!patient) {
    return res.status(404).json({
      success: false,
      message: 'Patient not found'
    });
  }

  res.json({
    success: true,
    message: 'Patient deleted successfully'
  });
});
